package br.ifirst.cemec.entidades;

import java.util.Date;

/**
 *
 * @author Iagho
 */
public class CalendarioTratamento {

    private int cdTratamento;
    private Date dtIni;
    private Date dtFin;
    private int seg;
    private int ter;
    private int qua;
    private int qui;
    private int sex;
    private int sab;
    private int dom;
    private int todosDias;

    public int getTodosDias() {
        return todosDias;
    }

    public void setTodosDias(int todosDias) {
        this.todosDias = todosDias;
    }

    public int getCdTratamento() {
        return cdTratamento;
    }

    public void setCdTratamento(int cdTratamento) {
        this.cdTratamento = cdTratamento;
    }

    public Date getDtIni() {
        return dtIni;
    }

    public void setDtIni(Date dtIni) {
        this.dtIni = dtIni;
    }

    public Date getDtFin() {
        return dtFin;
    }

    public void setDtFin(Date dtFin) {
        this.dtFin = dtFin;
    }

    public int getSeg() {
        return seg;
    }

    public void setSeg(int seg) {
        this.seg = seg;
    }

    public int getTer() {
        return ter;
    }

    public void setTer(int ter) {
        this.ter = ter;
    }

    public int getQua() {
        return qua;
    }

    public void setQua(int qua) {
        this.qua = qua;
    }

    public int getQui() {
        return qui;
    }

    public void setQui(int qui) {
        this.qui = qui;
    }

    public int getSex() {
        return sex;
    }

    public void setSex(int sex) {
        this.sex = sex;
    }

    public int getSab() {
        return sab;
    }

    public void setSab(int sab) {
        this.sab = sab;
    }

    public int getDom() {
        return dom;
    }

    public void setDom(int dom) {
        this.dom = dom;
    }
}
